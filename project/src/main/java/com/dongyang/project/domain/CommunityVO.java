package com.dongyang.project.domain;

public class CommunityVO {
	private String tid = "";
	private String site_tid = "";
	private String user_master_tid = "";
	private String user_name = "";
	private String title = "";
	private String contents = "";
	private String create_date = "";
	private String modify_date = "";
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getSite_tid() {
		return site_tid;
	}
	public void setSite_tid(String site_tid) {
		this.site_tid = site_tid;
	}
	public String getUser_master_tid() {
		return user_master_tid;
	}
	public void setUser_master_tid(String user_master_tid) {
		this.user_master_tid = user_master_tid;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	public String getModify_date() {
		return modify_date;
	}
	public void setModify_date(String modify_date) {
		this.modify_date = modify_date;
	}
}

package com.dongyang.project.domain;

public class SaleVO {
	private String tid = "";
	private String site_tid = "";
	private String product_list_tid = "";
	private String product_name = "";
	private String product_code = "";
	private String price = "";
	private String count = "";
	private String status = "";
	private String create_date = "";
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getSite_tid() {
		return site_tid;
	}
	public void setSite_tid(String site_tid) {
		this.site_tid = site_tid;
	}
	public String getProduct_list_tid() {
		return product_list_tid;
	}
	public void setProduct_list_tid(String product_list_tid) {
		this.product_list_tid = product_list_tid;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public String getProduct_code() {
		return product_code;
	}
	public void setProduct_code(String product_code) {
		this.product_code = product_code;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	
}
